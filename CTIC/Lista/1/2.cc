/*
	Presente un ejemplo del uso de los operadores de asignación y aritméticos.
*/
#include <iostream>

using namespace std;

int main(){

	float a = 23.0, b = 20.0;
	
	cout << "La suma es " << a + b << "." << endl;
	cout << "La resta es " << a - b << "." << endl;
	cout << "La multiplicación es " << a * b << "." << endl;
	cout << "La división es " << a / b << "." << endl;
	
	return 0;
}