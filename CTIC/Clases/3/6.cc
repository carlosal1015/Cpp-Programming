#include <iostream>
using namespace std;

int main (){

	cout << "Ejemplo c" << endl;
	for(int c = 7; c <= 77; c = c + 1){
		cout << c << endl;
	}

	cout << "Ejemplo d" << endl;
	
	for(int d = 20; d >= 2; d = d -2){
		cout << d << endl;
	}

	cout << "Ejemplo e" << endl;

	for(int e = 0; e >= -9; e = e - 1){
		cout << e << endl;
	}

	cout << "Ejemplo f" << endl;
	
	for(int f = 2; f <= 20; f = f + 3){
		cout << f << endl;
	}

	return 0;
}

