// di while (Validando el ingreso de valores negativos)
#include <iostream>
using namespace std;

int main(){

for(int i = 0; i < 3; i++){
	cout << "i= " << i+1 << endl;
	getchar();
	cout << "j = " << endl;
	for(int j = 0; j < 4; j++){
		cout << j+1 << " ";
		getchar();
	}
	cout << endl;
}
return 0;

}