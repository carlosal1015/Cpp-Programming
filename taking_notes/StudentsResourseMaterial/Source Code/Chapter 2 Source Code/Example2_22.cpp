
// This program illustrates how output statements work.
  
#include <iostream>
 
using namespace std; 

int main() 
{
    int a, b;
 
    a = 65;                             //Line 1
    b = 78;                             //Line 2

    cout << 29 / 4 << endl;             //Line 3
    cout << 3.0 / 2 << endl;            //Line 4
    cout << "Hello there.\n";           //Line 5
    cout << 7 << endl;                  //Line 6
    cout << 3 + 5 << endl;              //Line 7
    cout << "3 + 5";                    //Line 8
    cout << " **";                      //Line 9 
    cout << endl;                       //Line 10
    cout << 2 + 3 * 6 << endl;          //Line 11
    cout << "a" << endl;                //Line 12
    cout << a << endl;                  //Line 13
    cout << b << endl;                  //Line 14
    
    return 0;
}
